class Solution {
public:
    bool canMeasureWater(int x, int y, int z) {
        if(x == 0 || y == 0)
            return z == x || z == y;
        
        int t = gcd(x, y);
        return z % t == 0 && (x + y >= z);
    }

    int gcd(int a, int b)
    {
        return b == 0 ? a : gcd(b, a % b);
    }
};

